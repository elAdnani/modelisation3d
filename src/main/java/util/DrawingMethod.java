package util;
/**
 * 
 * Cette classe sert à énumerer les manières de dessiner.</br>
 * - WIREFRAME est un moyen de dessin qui affiche les contours des faces.</br>
 * - SOLID est un moyen de dessin qui affiche les faces sans les contours.</br>
 * - BOTH est un moyen de dessin qui n'affiche à la fois les faces avec les contours.</br>
 */
public enum DrawingMethod {
	WIREFRAME, SOLID, BOTH;
}
